
# Menampilkan judul
print("------Luas dan keliling Lingkaran------")

# input jari-jari
r = float(input("Masukkan nilai jari-jari: "))

# penghitungan luas
luas_lingkaran = 3.14 * (r**2)

# penghitungan keliling
keliling_lingkaran = 2 * 3.14 * r

# Menampilkan
print("Luas lingkaran = {}".format(luas_lingkaran))
print("Keliling lingkaran = {}".format(keliling_lingkaran))