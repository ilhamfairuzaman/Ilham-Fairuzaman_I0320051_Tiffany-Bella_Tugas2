# Menghitung luas persegi panjang
panjang = float(input("Masukkan panjang: "))
lebar = float(input("Masukkan lebar: "))
luas_pp = panjang * lebar
print(luas_pp)

# Menghitung luas lingkaran
r = float(input("Masukkan jari jari lingkaran: "))
luas_lingkaran = 3.14 * r**2
print(luas_lingkaran)

# Menghitung luas kubus
sisi = float(input("Masukkan panjang rusuk kubus: "))
luas_kubus = 6 * sisi **2
print(luas_kubus)

# Program Konversi suhu celcius ke fahrenheit
celcius = float(input("Masukkan suhu dalam Celcius: "))
konversicf = (9/5 * celcius) + 32
print(konversicf)

# Program Konversi suhu reamur ke kelvin
reamur = float(input("Masukkan suhu dalam reamur: "))
konversirk = (5/4 * reamur) + 273
print(konversirk)



